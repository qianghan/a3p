#!/usr/bin/env node
/**
 * Generate examples manifest for API routes.
 *
 * Scans examples/{name}/plugin.json and writes:
 *   1. apps/web-next/examples-manifest.json  (JSON, for other tooling)
 *   2. apps/web-next/src/generated/examples-manifest.ts  (TypeScript module)
 *
 * The TypeScript module is the PRIMARY source for Next.js API routes.
 * It lives inside src/ so webpack/turbopack always compiles and bundles it,
 * even on Vercel where runtime fs access to project-root files is unavailable.
 *
 * Usage:  node bin/generate-examples-manifest.cjs
 */

const fs = require('fs');
const path = require('path');

const ROOT = process.env.MONOREPO_ROOT
  ? path.resolve(process.env.MONOREPO_ROOT)
  : path.resolve(__dirname, '..');

const EXAMPLES_DIR = path.join(ROOT, 'examples');
const EXCLUDE_FILE = path.join(EXAMPLES_DIR, '.cdn-build-exclude');

function loadCdnBuildExclude() {
  const set = new Set();
  if (fs.existsSync(EXCLUDE_FILE)) {
    for (const line of fs.readFileSync(EXCLUDE_FILE, 'utf8').split(/\r?\n/)) {
      const t = line.replace(/#.*/, '').trim();
      if (t) set.add(t);
    }
  }
  if (set.size === 0) {
    set.add('intelligent-dashboard');
  }
  return set;
}

/** Same names as examples/.cdn-build-exclude (not on main product / CDN manifest). */
const EXCLUDE_FROM_EXAMPLES_MANIFEST = loadCdnBuildExclude();
const CDN_DIR = path.join(ROOT, 'apps', 'web-next', 'public', 'cdn', 'plugins');
const DIST_DIR = path.join(ROOT, 'dist', 'plugins');
const OUT_JSON = path.join(ROOT, 'apps', 'web-next', 'examples-manifest.json');
const OUT_TS = path.join(ROOT, 'apps', 'web-next', 'src', 'generated', 'examples-manifest.ts');

function toCamelCase(s) {
  return s.replace(/-([a-z])/g, (_, c) => c.toUpperCase());
}

function toPascalCase(s) {
  const camel = toCamelCase(s);
  return camel.charAt(0).toUpperCase() + camel.slice(1);
}

function generate() {
  let entries = [];

  if (fs.existsSync(EXAMPLES_DIR)) {
    const dirs = fs.readdirSync(EXAMPLES_DIR);

    entries = dirs
      .filter((d) => {
        if (EXCLUDE_FROM_EXAMPLES_MANIFEST.has(d)) return false;
        const pj = path.join(EXAMPLES_DIR, d, 'plugin.json');
        return fs.existsSync(pj);
      })
      .map((d) => {
        const manifest = JSON.parse(
          fs.readFileSync(path.join(EXAMPLES_DIR, d, 'plugin.json'), 'utf8'),
        );
        const rawAuthor = manifest.author;
        const authorName = typeof rawAuthor === 'string' ? rawAuthor : rawAuthor?.name;
        const authorEmail = typeof rawAuthor === 'object' ? rawAuthor?.email : undefined;
        const rawRoutes = manifest.frontend?.routes || [];
        const camelName = toCamelCase(d);

        const hasCdnBundle = fs.existsSync(path.join(CDN_DIR, d, '1.0.0', `${d}.js`));
        const hasDistBundle = fs.existsSync(path.join(DIST_DIR, d, '1.0.0', `${d}.js`));

        return {
          name: camelName,
          dirName: d,
          displayName: manifest.displayName || d,
          description: manifest.description || '',
          category: manifest.category || 'example',
          author: authorName || 'NAAP Examples',
          authorEmail: authorEmail || undefined,
          version: '1.0.0',
          icon: manifest.frontend?.navigation?.icon || 'Box',
          routes: [`/plugins/${d}`, `/plugins/${d}/*`],
          originalRoutes: rawRoutes,
          order: manifest.frontend?.navigation?.order ?? 99,
          globalName: `NaapPlugin${toPascalCase(d)}`,
          keywords: manifest.keywords || [],
          license: manifest.license || 'MIT',
          repository: manifest.repository || `https://github.com/livepeer/naap/tree/main/examples/${d}`,
          hasBuild: hasCdnBundle || hasDistBundle,
        };
      })
      .sort((a, b) => a.displayName.localeCompare(b.displayName));
  }

  // Safety: if the TS file already exists with content and the scan found 0
  // entries, keep the existing file to avoid overwriting a valid committed manifest.
  if (entries.length === 0 && fs.existsSync(OUT_TS)) {
    const existing = fs.readFileSync(OUT_TS, 'utf8');
    if (existing.includes('EXAMPLES_MANIFEST') && !existing.includes('[] = [];')) {
      console.log(`[examples-manifest] WARNING: scan found 0 examples but existing TS manifest has content — keeping existing file`);
      console.log(`  → ${path.relative(ROOT, OUT_TS)} (preserved)`);
      return;
    }
  }

  // Write JSON (for tooling / scripts)
  fs.mkdirSync(path.dirname(OUT_JSON), { recursive: true });
  fs.writeFileSync(OUT_JSON, JSON.stringify(entries, null, 2));

  // Write TypeScript module (primary source for Next.js API routes)
  fs.mkdirSync(path.dirname(OUT_TS), { recursive: true });
  const tsContent = [
    '// Auto-generated by bin/generate-examples-manifest.cjs — DO NOT EDIT',
    '',
    'export interface ExampleManifestEntry {',
    '  name: string;',
    '  dirName: string;',
    '  displayName: string;',
    '  description: string;',
    '  category: string;',
    '  author: string;',
    '  authorEmail?: string;',
    '  version: string;',
    '  icon: string;',
    '  routes: string[];',
    '  originalRoutes: string[];',
    '  order: number;',
    '  globalName: string;',
    '  keywords: string[];',
    '  license: string;',
    '  repository: string;',
    '  hasBuild: boolean;',
    '}',
    '',
    `export const EXAMPLES_MANIFEST: ExampleManifestEntry[] = ${JSON.stringify(entries, null, 2)};`,
    '',
    'export default EXAMPLES_MANIFEST;',
    '',
  ].join('\n');
  fs.writeFileSync(OUT_TS, tsContent);

  const count = entries.length;
  const label = count === 0 ? 'No examples/ directory found — wrote empty manifest' : `Wrote ${count} example(s)`;
  console.log(`[examples-manifest] ${label}`);
  console.log(`  → ${path.relative(ROOT, OUT_JSON)}`);
  console.log(`  → ${path.relative(ROOT, OUT_TS)}`);
}

generate();
